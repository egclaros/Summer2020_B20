package day18_ForLoop;

public class ForLoop_Practice {
    public static void main(String[] args) {

        for ( int i=0;i<=5; i++){
            System.out.println("Massimo");
        }
        System.out.println("Done");

        for ( int i=0;i<=10; i++){
            System.out.print(i);
        }
        System.out.println(":Done");

        for ( int i=10;i>0; i--){
            System.out.print(i);
        }
        System.out.println(":Done");


    }

}
