package day03_sequences_Variables;

public class EscapeSequences {
    public static void main (String [] args){
        System.out.println("Hello Everyone \nMy name is Omer");
        System.out.println(" \tI am in NH\n");
        System.out.println(" \n\n \t\t\tI love programing");

        System.out.println("\\");
        System.out.println("/");
        System.out.println("\\\\");
        System.out.println("//");
        System.out.println("My name is \'Omer\'");
        System.out.println("My name is 'Omer'");
        System.out.println("My favarite TV-series:\" Game of Thrones \" ");

    }
}
