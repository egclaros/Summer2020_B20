package day05;

public class RadiusAreaPerimeter {
    public static void main(String[] args) {
        int radius = 3;
        double pi = 3.14;
        double perimeter = 2 * radius*pi ;
        System.out.println("Perimeter ="+perimeter+ " cm");

        double area = radius*radius*pi;
        System.out.println("Area is :" + area+ " cm");




    }
}
