package day22_NestesLoop;

public class Nested_Loop {
    public static void main(String[] args) {
//  1 2 3 4 5 ==> 5 times write

        for (int i=1; i<=5 ; i++){
            System.out.print(i+" ");
        }
        System.out.println();

        for (int i=1; i<=5 ; i++) {
            System.out.print(i + " ");
        }
        System.out.println();

        for (int i=1; i<=5 ; i++) {
            System.out.print(i + " ");
        }
        System.out.println();

        for (int i=1; i<=5 ; i++) {
            System.out.print(i + " ");
        }
        System.out.println();

        for (int i=1; i<=5 ; i++) {
            System.out.print(i + " ");
        }
        System.out.println();
    }
}
