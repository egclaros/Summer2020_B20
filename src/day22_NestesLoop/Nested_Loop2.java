package day22_NestesLoop;

public class Nested_Loop2 {
    public static void main(String[] args) {

//  1 2 3 4 5 ==> 5 times write

        for (int j=1; j<=5 ; j++){
            for (int i=1; i<=5 ; i++) {
                System.out.print(i + " ");
            }
            System.out.println();

        }



    }
}
