package day07_Unary_ShortHand;

public class Unary {
    public static void main(String[] args) {
        System.out.println(-1 - -10);


//  ++
        int a = 20;
        ++a;
        System.out.println(a);


// --:
        int b = 20;
        --b;
        System.out.println(b);

        int x = 100;
        System.out.println( ++x);

        int y = 200;
        System.out.println(--y);

        int z= 20;
        z++;
        System.out.println(z++);

    }
}
