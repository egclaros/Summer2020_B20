package day19_ForLoop;

public class LoopWitoutCurlyBraces {
    public static void main(String[] args) {
            int i=0;
        for (i=5; i<10; i++){
            System.out.println("Hello");
            System.out.println("How are you?");
        }

        System.out.println("====================");

        for (i=1; i<=5; i++)
            System.out.println("Hello");
            System.out.println("How are you?");


    }
}
